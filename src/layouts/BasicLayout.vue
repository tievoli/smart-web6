<template>
  <pro-layout
    v-model:collapsed="baseState.collapsed"
    v-model:selectedKeys="baseState.selectedKeys"
    v-model:openKeys="baseState.openKeys"
    v-bind="state"
    :loading="loading"
    :breadcrumb="{ routes: breadcrumb }"
    iconfont-url="//at.alicdn.com/t/font_2804900_nzigh7z84gc.js"
  >
    <template #menuHeaderRender>
      <a>
        <img src="../../public/antdv-pro-logo.svg" />
        <h1>Pro Layout</h1>
      </a>
    </template>

    <!-- custom right-content -->
    <template #rightContentRender>
      <div style="margin-right: 12px">
        <a-avatar shape="square" size="small">
          <template #icon>
            <UserOutlined />
          </template>
        </a-avatar>
      </div>
    </template>

    <!-- custom breadcrumb itemRender  -->
    <template #breadcrumbRender="{ route, params, routes }">
      <span v-if="routes.indexOf(route) === routes.length - 1">
        {{ route.breadcrumbName }}
      </span>
      <router-link v-else :to="{ path: route.path, params }">
        {{ route.breadcrumbName }}
      </router-link>
    </template>

    <!-- content begin -->
    <router-view v-slot="{ Component }">
      <WaterMark :content="watermarkContent">
        <component :is="Component" />
      </WaterMark>
    </router-view>

    <template #footerRender>
      <GlobalFooter :links="links" :copyright="copyright" />
    </template>

    <!-- content end -->
    <FooterToolbar>
      <template #extra>
        <span>FooterToolbar</span>
      </template>
      <a-space>
        <span :disabled="state.layout !== 'mix'">
          <span style="margin-right: 8px">SplitMenus:</span>
          <a-switch
            v-model:checked="state.splitMenus"
            :disabled="state.layout !== 'mix'"
            checked-children="ON"
            un-checked-children="OFF"
          />
        </span>
        <a-button @click="handlePageLoadingClick">Page Loading</a-button>
        <a-select v-model:value="state.navTheme" style="width: 100px">
          <a-select-option value="light">Light</a-select-option>
          <a-select-option value="dark">Dark</a-select-option>
        </a-select>
        <a-select v-model:value="state.layout" style="width: 100px">
          <a-select-option value="side">Side</a-select-option>
          <a-select-option value="top">Top</a-select-option>
          <a-select-option value="mix">Mix</a-select-option>
        </a-select>
      </a-space>
    </FooterToolbar>
  </pro-layout>
</template>

<script>
import {
  computed,
  defineComponent,
  reactive,
  ref,
  watchEffect,
  onMounted,
} from "vue";
import { useRouter } from "vue-router";
import { UserOutlined } from "@ant-design/icons-vue";
import {
  getMenuData,
  clearMenuItem,
  WaterMark,
  FooterToolbar,
  GlobalFooter,
} from "@ant-design-vue/pro-layout";

export default defineComponent({
  name: "BasicLayout",
  components: {
    WaterMark,
    FooterToolbar,
    UserOutlined,
    GlobalFooter,
  },
  setup() {
    const loading = ref(false);
    const watermarkContent = ref("");
    const router = useRouter();
    const { menuData } = getMenuData(clearMenuItem(router.getRoutes()));

    const links = [
      {
        title: "常见问题",
        href: "#",
      },
      {
        title: "平台介绍",
        href: "#",
      },
      {
        title: "更新日志",
        href: "#",
      },
    ];

    const copyright = "2021 @ Tievoli";

    const baseState = reactive({
      selectedKeys: [],
      openKeys: [],
      // default
      collapsed: false,
    });

    const state = reactive({
      menuData,
      splitMenus: true,
      // title: 'ProLayout',
      // logo: 'https://alicdn.antdv.com/v2/assets/logo.1ef800a8.svg',
      navTheme: "light",
      layout: "mix",
      fixSiderbar: true,
      fixedHeader: true,
    });

    const breadcrumb = computed(() =>
      router.currentRoute.value.matched.concat().map((item) => {
        return {
          path: item.path,
          breadcrumbName: item.meta.title || "",
        };
      })
    );

    const handleCollapsed = () => {
      baseState.collapsed = !baseState.collapsed;
    };

    watchEffect(() => {
      if (router.currentRoute) {
        const matched = router.currentRoute.value.matched.concat();
        baseState.selectedKeys = matched
          .filter((r) => r.name !== "index")
          .map((r) => r.path);
        baseState.openKeys = matched
          .filter((r) => r.path !== router.currentRoute.value.path)
          .map((r) => r.path);
      }
    });

    function handlePageLoadingClick() {
      loading.value = true;
      setTimeout(() => {
        loading.value = false;
      }, 2000);
    }

    onMounted(() => {
      setTimeout(() => {
        watermarkContent.value = "God Bless You!!!";
      }, 2000);
    });

    return {
      watermarkContent,
      baseState,
      state,
      loading,
      breadcrumb,
      links,
      copyright,
      handlePageLoadingClick,
      handleCollapsed,
    };
  },
});
</script>
